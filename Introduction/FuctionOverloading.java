class Multiply{

    static int mul(int a,int b){return a*b;}  
    static int mul(int a,int b,int c){return a*b*c;}  
    }  
    class FuctionOverloading{  
        public static void main(String[] args){  
        	System.out.println("Program for Function Overloading");
                System.out.println(Multiply.mul(11,12));  
			System.out.println(Multiply.mul(11,12,13));  
                            }}  